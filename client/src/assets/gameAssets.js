// Utility functions
export const getGameAsset = (gameType, assetType = 'thumbnail') => {
  const game = gameAssets[gameType?.toLowerCase()];
  return game ? game[assetType] : gameAssets.bgmi[assetType];
};

export const getGameInfo = (gameType) => {
  return gameAssets[gameType?.toLowerCase()] || gameAssets.bgmi;
};

export const getGamesByCategory = (category) => {
  return Object.entries(gameAssets)
    .filter(([_, game]) => game.category === category)
    .map(([key, game]) => ({ key, ...game }));
};

export const getAllGames = () => {
  return Object.entries(gameAssets).map(([key, game]) => ({ key, ...game }));
};

